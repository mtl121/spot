package cn.lvb.bean;

public class Estimate {
	private String Esti_id;
	private String Esti_content;
	private String Esti_star;
	private String Tou_phone;
	private String Spot_id;
	public Estimate() {
		super();
	}
	/**
	 * @return the esti_id
	 */
	public String getEsti_id() {
		return Esti_id;
	}
	/**
	 * @param esti_id the esti_id to set
	 */
	public void setEsti_id(String esti_id) {
		Esti_id = esti_id;
	}
	/**
	 * @return the esti_content
	 */
	public String getEsti_content() {
		return Esti_content;
	}
	/**
	 * @param esti_content the esti_content to set
	 */
	public void setEsti_content(String esti_content) {
		Esti_content = esti_content;
	}
	/**
	 * @return the esti_star
	 */
	public String getEsti_star() {
		return Esti_star;
	}
	/**
	 * @param esti_star the esti_star to set
	 */
	public void setEsti_star(String esti_star) {
		Esti_star = esti_star;
	}
	/**
	 * @return the tou_phone
	 */
	public String getTou_phone() {
		return Tou_phone;
	}
	/**
	 * @param tou_phone the tou_phone to set
	 */
	public void setTou_phone(String tou_phone) {
		Tou_phone = tou_phone;
	}
	/**
	 * @return the spot_id
	 */
	public String getSpot_id() {
		return Spot_id;
	}
	/**
	 * @param spot_id the spot_id to set
	 */
	public void setSpot_id(String spot_id) {
		Spot_id = spot_id;
	}
	
}
